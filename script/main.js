const canvas = document.getElementById("sparkleCanvas");
const ctx = canvas.getContext("2d");
canvas.width = window.innerWidth;
canvas.height = window.innerHeight;

let sparkles = [];

function createSparkle() {
    return {
        x: Math.random() * canvas.width,
        y: Math.random() * canvas.height,
        radius: Math.random() * 6 + 4, // larger size: 4 to 10 px
        opacity: Math.random() * 0.6 + 0.4,
        speed: Math.random() * 0.5 + 0.2
    };
}
function drawStar(ctx, x, y, outerRadius, points, innerRadius, color, opacity) {
    const step = Math.PI / points;
    ctx.beginPath();
    for (let i = 0; i < 2 * points; i++) {
        const r = (i % 2 === 0) ? outerRadius : innerRadius;
        const angle = i * step - Math.PI / 2;
        const sx = x + r * Math.cos(angle);
        const sy = y + r * Math.sin(angle);
        ctx.lineTo(sx, sy);
    }
    ctx.closePath();
    ctx.fillStyle = `rgba(${color}, ${opacity})`;
    ctx.shadowColor = `rgba(${color}, 0.8)`;
    ctx.shadowBlur = 15;
    ctx.fill();
    ctx.shadowBlur = 0;
}
function drawSparkles() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    sparkles.forEach(s => {
        const outer = s.radius;
        const inner = outer * 0.5;
        drawStar(ctx, s.x, s.y, outer, 5, inner, "255, 223, 0", s.opacity);
    });
}
function initSparkles() {
    sparkles = [];
    for (let i = 0; i < 150; i++) {
        sparkles.push(createSparkle());
    }
}

function updateSparkles() {
    sparkles.forEach(s => {
        s.y -= s.speed;
        s.opacity -= 0.003;
        if (s.opacity <= 0 || s.y < 0) {
            Object.assign(s, createSparkle());
            s.y = canvas.height;
        }
    });
}

function animateSparkles() {
    drawSparkles();
    updateSparkles();
    requestAnimationFrame(animateSparkles);
}

initSparkles();
animateSparkles();
